# Este es un ejemplo de uso de la función append

# Hay una lista de nombres llamada nombres

# Pedimos al usuario que introduzca otro nombre, este se añadirá a la lista y la lista resultante se imprimirá por pantalla

names = ['Ana', 'Samuel', 'María', 'Pablo'] #Lista de nombres

names.append(input("Introduce otro nombre: "));

print(names)